#양 쪽 모두 참이기 때문에 참이다.
r1 = a1 < 20 and a2 < 20
print('r1 :', r1)

#왼쪽이 거짓이므로 거짓이다.
r2 = a1 > 20 and a2 < 20
print('r2 :', r2)

# 양쪽 모두 거짓이므로 거짓
r3 = a1 > 20 or a2 > 20
print('r3: ', r3)

# 한쪽이 참이므로 참
r4 = a1 < 20 or a2 > 20
print('r4: ', r4)

#우측 수식의 결과를 반대로 부정한다.
r5 = not a1 < 20
